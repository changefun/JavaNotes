public class StringToWallper{
    public static void main(String[] args){
        Integer i = Integer.parseInt("123");
        System.out.println(i);
        Byte b = Byte.parseByte("127");
        System.out.println(b);
        Short s = Short.parseShort("34");
        System.out.println(s);
        Long l = Long.parseLong("3423");
        System.out.println(l);
        Float f = Float.parseFloat("12.3f");
        System.out.println(f);
        Double d = Double.parseDouble("232.22");
        System.out.println(d);
        Boolean bl = Boolean.parseBoolean("true");
        System.out.println(bl);
    }
}
